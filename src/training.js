function hashTime(timeInMinutes) {
  if (timeInMinutes > 100) timeInMinutes = 100;
  return Math.log10(timeInMinutes / 10);
}

function reverseHashTime(num) {
  return Math.floor(Math.pow(10, num) * 10);
}

